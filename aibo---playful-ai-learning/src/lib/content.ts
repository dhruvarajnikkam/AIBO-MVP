export type ChallengeType = 
  | 'tap_right_answer'
  | 'drag_and_drop' 
  | 'fill_in_blank' 
  | 'match_pairs' 
  | 'sort_sequence' 
  | 'analogy_card' 
  | 'spot_the_ai' 
  | 'build_concept' 
  | 'scenario_quest' 
  | 'rapid_fire'
  | 'before_after_exp'
  | 'mystery_concept'
  | 'step_debugger'
  | 'argument_builder'
  | 'link_builder';

export interface Challenge {
  id: string;
  type: ChallengeType;
  question: string;
  options?: string[];
  correctAnswer?: string | string[] | number | { [key: string]: string } | any;
  explanation: string;
  // Specific fields for different types
  buckets?: { id: string; label: string }[];
  items?: { id: string; text: string; correct_bucket?: string; has_ai?: boolean; explanation?: string; belongs?: boolean; connection_reason?: string }[];
  pairs?: { left: string; right: string }[];
  steps?: { id: string; text: string; explanation?: string; question?: string; options?: string[]; correct_option?: number }[];
  correctOrder?: string[];
  analogy?: { text: string; options: string[] };
  sentence?: string;
  targetSentence?: string;
  chipsCorrect?: string[];
  chipsDistractor?: string[];
  scenario?: string;
  studentRole?: string;
  decisionPrompt?: string;
  scenarioOptions?: { id: string; text: string; consequence: string; is_best: boolean; explanation: string }[];
  questions?: any[]; // For rapid fire and confidence calibration
  xpValue?: number;
  
  // New fields for additional methods
  experiment?: {
    conditionA: { name: string; accuracy: number; results: boolean[] };
    conditionB: { name: string; accuracy: number; results: boolean[] };
    reflectionQuestions: { question: string; options: string[]; correctAnswer: string; explanation: string }[];
  };
  mystery?: {
    concept: string;
    clues: string[];
    hotKeywords: string[];
    warmKeywords: string[];
  };
  argument?: {
    statement: string;
    cards: { text: string; side: 'for' | 'against'; note: string }[];
    closingInsight: string;
  };
  linkBuilder?: {
    centralConcept: string;
    pool: { text: string; belongs: boolean; reason: string }[];
    debrief: string;
  };
}

export interface Lesson {
  id: string;
  title: string;
  description: string;
  difficulty: 'Easy' | 'Medium' | 'Hard';
  challenges: Challenge[];
  isBonus?: boolean;
  isChest?: boolean;
  isChallenge?: boolean;
}

export interface Module {
  id: string;
  title: string;
  difficulty: 'Easy' | 'Medium' | 'Hard';
  lessons: Lesson[];
}

